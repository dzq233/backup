package Demo2;

public class Wang {
    private int age;
    private String name;

    public Wang(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public Wang() {
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}

